package exercise.ch37;

public class Baby {
	//속성
	private String name;
	private int age;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public Baby(String name) {
		super();
		this.name = name;
	}
	
	
	
	
}
